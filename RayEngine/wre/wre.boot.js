WRE.boot = function (callback) {
    WRE.runtime._init();
    $.when(
        WRE.view._init(),
        WRE.ui._init()
    ).then(function () {
        WRE.view._build(function () {
            WRE.ui._build(function () {
                callback();
            });
        });
    });
};



